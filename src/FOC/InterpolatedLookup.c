#include "InterpolatedLookup.h"



float InterpolatedLookup_Interpolate( Interpolation* i)
{
    if ( i->x < i->x0 )
        return i->y0;

    if ( i->x > i->x1 )
        return i->y1;

    return i->y0 + ( ( i->x - i->x0 ) / ( i->x1 - i->x0 ) ) * ( i->y1 - i->y0 );
}


